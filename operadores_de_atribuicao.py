## Atribuição simples
saldo = 500
print(saldo)
saldo = 200
print(saldo)
## Atribuição com adição
saldo += 10
print(saldo)
## Atribuição com subtração
saldo -= 5
print(saldo)
## Atribuição com muntiplicação
saldo *= 10
print(saldo)
## Atribuição com divisão
saldo /= 2
print(saldo)
saldo //= 2
print(saldo)
## Atribuição com módulo
saldo %= 3
print(saldo)
## Atribuição com exponenciação
saldo **= 2
print(saldo)
